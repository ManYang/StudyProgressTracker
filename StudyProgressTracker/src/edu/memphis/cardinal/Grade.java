package edu.memphis.cardinal;

public class Grade {
	private int studentID;
	private String studentName;
	private String courseID;
	private String courseName;
	private String grade;
	private int status;
	public Grade(int sID,String sName, String cID,String cName, String sGrade,int sStatus)
	{
		studentID=sID;
		studentName=sName;
		courseID=cID;
		courseName=cName;
		grade =sGrade;
		status=sStatus;
	}
	public void setStudentID(int sID)
	{
		studentID=sID;
	}
	public void setStudentName(String sName)
	{
		studentName=sName;
	}
	public void setCourseID(String cID)
	{
		courseID=cID;
	}
	public void setCourseName(String cName)
	{
		courseName=cName;
	}
	public void setGrade(String sGrade)
	{
		grade =sGrade;
	}
	public void setStatus(int sStatus)
	{
		status=sStatus;
	}
	public int getStudentID()
	{
		return studentID;
	}
	public String getStudentName()
	{
		return studentName;
	}
	public String getCourseID()
	{
		return courseID;
	}
	public String getCourseName()
	{
		return courseName;
	}
	public String getGrade()
	{
		return grade;
	}
	public int getStatus()
	{
		return status;
	}
}
